package beans;

/**
 *
 * @author Asier
 */
public class Personalidad {
    private String nick;
    private String nombre;
    
    public Personalidad(String nick,String nombre){
        this.nick = nick;
        this.nombre = nombre;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
